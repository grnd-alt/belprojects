$('document').ready(function(){
	$('#menu-wrapper').css("margin-top",$('#header').css("height"));
	$('#sidebar-image').on('click',function(){
		if($('#menu-wrapper').css("display")=='block'){
			$('#menu-wrapper').css('display','none')}
		else{
			$('#menu-wrapper').css('display','block');
		}
	});
	$(".menu-text").on("click",function(event){
		var id = event.target.id;
		alert(id);
		//event.targer.css("background-color","purple");
		$.ajax({
			url:"/control",
			type:"POST",
			data:{button:id},
			success:function(result){
				$('body').html(result);
				$("#"+id).css("background-color","purple");
				$("#"+id+"_wrapper").css("background-color","purple");
			}
		})
	})
});